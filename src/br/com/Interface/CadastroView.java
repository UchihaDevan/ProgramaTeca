    private void upFoto() {
        JFileChooser jfc = new JFileChooser();
        jfc.setDialogTitle("Selecionar foto de perfil");
        jfc.setFileFilter(new FileNameExtensionFilter("Arquivo de imagens (*.PNG,*.JPG,*.JPEG)", "png", "jpg", "jpeg"));
        int resultado = jfc.showOpenDialog(this);
        if (resultado == JFileChooser.APPROVE_OPTION) {
            try {
                fis = new FileInputStream(jfc.getSelectedFile());
                tamanho = (int) jfc.getSelectedFile().length();
                Image foto = ImageIO.read(jfc.getSelectedFile()).getScaledInstance(jlFotoPerfil.getWidth(), jlFotoPerfil.getHeight(), Image.SCALE_SMOOTH);
                jlFotoPerfil.setIcon(new ImageIcon(foto));
                jlFotoPerfil.updateUI();
            } catch (Exception e) {
                System.out.println(e);
            }
        }
    }

    private void cadastrar() {
        String inserir = "insert into login (nome,email,foto,senha,data_nasc) values(?,?,?,?,?)";
        try {
            
            pst = con.prepareStatement(inserir);
            pst.setString(1, jtNome.getText());
            pst.setString(2, jtEmail.getText());
            pst.setString(3, jNasc.getText());
            pst.setString(4, jpSenha.getText());
            pst.setBlob(5, fis, tamanho);
        } catch (Exception e) {
            System.out.println("Erro no cadastro" + e);
        }
    }

